int	ft_str_is_uppercase(char *str)
{
	int i;

	if(!str)
		return (1);
	i = 0;
	while(str[i])
	{
		if (str[i] > 32 && str[i] < 128)
			i++;
		else
			return (0);
	}
	return (1);
}
